package model;
public class Amostra {
	
	private int totalAmostras;
	private Rocha rocha;
	private Coordenadas coordenada;
	
	
	
	
	
	public int getTotalAmostras() {
		return totalAmostras;
	}
	public void setTotalAmostras(int totalAmostras) {
		this.totalAmostras = totalAmostras;
	}
	public Rocha getRocha() {
		return rocha;
	}
	public void setRocha(Rocha rocha) {
		this.rocha = rocha;
	}
	public Coordenadas getCoordenada() {
		
		return coordenada;
	}
	public void setCoordenada(Coordenadas coordenada) {
		this.coordenada = coordenada;
	}
}
